							<h2 class="wow fadeIn article-subheader" data-wow-delay="1s">Moving forward...</h2>
							<p class="wow fadeIn" data-wow-delay="1.5s">Now that was a long time ago. Since then, the charity constantly grew with more and more support from individuals and groups of people across UK and Europe. The charity also has wide spread support from mainstream artists, rappers and public figures which has helped the charity highly. With over 475 volunteers around different parts of London and cities in the UK, it is fair to say the charity has given the youth a second chance to fix their community. From volunteers doing self led work in their schools to raise money to those doing school projects on the charity. We hope the charity is a stepping stone to help everyone get everyone to where they want and we hope it motivates everyone to become a better, respectful and honest person. All volunteers have left with amazing life experiences, new friends and something to put down on their CV and personal statement. </p>

							<p class="wow fadeIn" data-wow-delay="2s">The HTHUK team (both charities combined commonly known as HTHUK) is a team of devoted individuals. The charity not only helps those in need, but helps themselves maintain the transition they are desperate to make, to become a good member of the community. This is evident in the attitudes they have towards life, the change in personalities, their confidence and their courtesy to those around them. All of our volunteers are incredible individuals going through a journey, and we hope HTHUK has been a part of their life journey. Many, if not all of the volunteers have amazing life experiences as well as stories which has helped our personal journeys. We welcome new volunteers with a warm welcome, a bag of necessities, quick summary of what to expect for the day and Adnan's funny yet unexpected mood swings. </p>
