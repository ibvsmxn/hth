							<h2 class="wow fadeIn article-subheader" data-wow-delay="1s">The founding of HTH UK.</h2>
							<p class="wow fadeIn" data-wow-delay="1.5s">The charity had first started from several random acts of kindness, which had been influenced be the deficient experiences the founder had faced in the early stages of his life. With various different demotivating occurrences he had faced, the acts of kindness had naturally pushed him to get out of the stereotypes set on young teenagers, to become better and to help better others. Despite the negativity the founder faced, Adnan Davod founded the charity, Helping The Homeless UK (HTHUK), at the age of 16. Not only did the charity help those who were desperately in need, he helped this in similar predicaments as him by volunteering. By doing so, Adnan transitioned and had the fortitude of being supported by his amazing colleagues, Iqbal Tanay, Omar Butt, Nabhan Islam, Naveed Amiri, Daler Shira and Aaron Chen, who continuously took part in all charity events, input great ideas and were great people to work with. Later on in the year, Adnan gained a partnership with his local Asda, Wembley Park which helped him raise money and awareness for the charity. This gave HTHUK a source of funding when buying food and necessities for the homeless. </p>

							<h2 class="wow fadeIn article-subheader" data-wow-delay="1s">Making a Change UK.</h2>
							<p class="wow fadeIn" data-wow-delay="1.5s">Still feeling contempt, Adnan took the charity a step further almost straight away by starting Making A Change UK, as he thought you shouldn't just help people, but make a change to their lives to help them in the long term run too. It also meant, Making A Change UK didn't have to just help homeless people, but everyone in need. Looking at area the volunteers lived in, there are various problems the people in their community face. So, the first step of Making A Change was by helping a single mum of 3 pay for 2 months of rent. This rapidly grew to helping struggling families by paying for grocery shopping, talking to the youth, organising mini football tournaments and much more. The group had been invited to various to events to spread the word of their movement, which then led up to when they contributed in funding the school "Unity Girls High School'. The school was on the verge of closing, when a supporter of the charity had messaged the team who came down to show their support and help them out...which relieved their fears the same night as a team, school and as a community and keeping the school running. </p>

